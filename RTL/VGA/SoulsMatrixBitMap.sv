//
// pipeline (ff) to get the pixel color from the array 	 

//==----------------------------------------------------------------------------------------------------------------=
logic collision_d;

always_ff@(posedge clk or negedge resetN)
begin
	if(!resetN) begin
		RGBout <=	8'hFF;
		MazeBitMapMask  <=  MazeDefaultBitMapMask ;  //  copy default tabel 
		counter <= 3;
		unsafe_walk_on_street <= 0;
		collision_d <= 0;

	end
	else begin
		RGBout <= TRANSPARENT_ENCODING ; // default 
		
		if(counter == 0)begin
			unsafe_walk_on_street <= 1;
		end else unsafe_walk_on_street <= 0; 
		
		if (InsideRectangle == 1'b1 )	
			begin 
		   	case (MazeBitMapMask[offsetY_MSB][offsetX_MSB])
					 4'h0 : RGBout <= TRANSPARENT_ENCODING ;
					 4'h1 : RGBout <= TRANSPARENT_ENCODING ;
					 4'h2 : RGBout <= TRANSPARENT_ENCODING ;
					 4'h3 : RGBout <= TRANSPARENT_ENCODING ;
					 4'h4 : RGBout <= coin_colors[offsetY_LSB][offsetX_LSB] ; 

					 default:  RGBout <= TRANSPARENT_ENCODING ; 
				endcase
		collision_d <= collision_pacman_monster;

		if (counter == 3 && collision_pacman_monster && !collision_d && !runMode)
      begin
		MazeBitMapMask[0][17] <= 4'h0;  // clear entry
		counter <= counter-1;
		end
	   if (counter == 2 && collision_pacman_monster && !collision_d && !runMode)
      begin
		MazeBitMapMask[0][18] <= 4'h0;  // clear entry
		counter <= counter-1;
		end
	   if (counter == 1 && collision_pacman_monster && !collision_d && !runMode)
      begin
		MazeBitMapMask[0][19] <= 4'h0;  // clear entry
		counter <= counter-1;
		end
			end 

	end 
end

//==----------------------------------------------------------------------------------------------------------------=
// decide if to draw the pixel or not 
